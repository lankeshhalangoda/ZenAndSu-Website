// discoutnt calculate
function calculate_discount() {

    var price = document.getElementById("price_id").value;
    var discount = document.getElementById("discount_id").value;

    var f = new FormData();
    f.append("p", price);
    f.append("d", discount);

    var r = new XMLHttpRequest();
    r.onreadystatechange = function() {
        if (r.readyState == 4) {
            var t = r.responseText;

            if (t == 1) {

                Snackbar.show({
                    pos: 'bottom-right',
                    text: "Please enter price",
                    actionTextColor: '#17D1EE',
                    customClass: 'snack'
                });

            } else {
                // alert(t);
                document.getElementById("new_price_id").innerHTML = t;
                Snackbar.show({
                    pos: 'bottom-right',
                    text: "Total : LKR " + t + " /=",
                    actionTextColor: '#17D1EE',
                    customClass: 'snack'
                });
            }



        }
    };
    r.open("POST", "discount_calculate.php", true);
    r.send(f);

}


function coupon_function() {
    var v = document.getElementById("discount_id").value;

    if (v == null || v == 0) {
        document.getElementById("coupon_code").setAttribute("disabled", "disabled");
    } else {
        document.getElementById("coupon_code").removeAttribute("disabled");
    }

}